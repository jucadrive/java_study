package ch11;

import java.util.ArrayList;
import java.util.Collections;

public class Ex11_1 {

	public static void main(String[] args) {
		ArrayList list1 = new ArrayList(10);
		// boolean add(Object o) : ArrayList의 마지막에 객체를 추가. 성공하면 true 반환
		list1.add(new Integer(5));
		list1.add(new Integer(4));
		list1.add(new Integer(2));
		list1.add(new Integer(0));
		list1.add(new Integer(1));
		list1.add(new Integer(3));
		
		// List subList(int fromIndex, int toIndex) : 지정된 범위 사이에 있는 객체를 반환한다.(toIndex 포함 안 됨)
		ArrayList list2 = new ArrayList(list1.subList(1, 4));	 
		print(list1, list2);
		
		// void sort(Comparator c) : 지정된 비교자로 List를 정렬한다.
		Collections.sort(list1);
		Collections.sort(list2);
		print(list1, list2);
		
		// boolean containsAll(Collections c) : 지정된 객체 또는 Collection의 객체들이 Collection에 포함되어 있는지 확인한다.
		System.out.println("list1.containsAll(list2) : " + list1.containsAll(list2));
		
		list2.add("B");
		list2.add("C");
		list2.add(3, "A");	// 인덱스가 3인 곳에 "A"를 추가
		print(list1, list2);
		
		list2.set(3, "AA");	// 인덱스가 3인 곳을 "AA"로 변경
		print(list1, list2);
		
		// list1에서 list2와 겹치는 부분만 남기고 나머지는 삭제한다
		System.out.println("list1.retainAll(list2) : " + list1.retainAll(list2));
		
		print(list1, list2);
		
		// list2에서 list1에 포함된 객체들을 삭제한다
		for(int i = list2.size() - 1; i >= 0; i--) {
			if(list1.contains(list2.get(i))) {
				list2.remove(i);	// 인덱스가 i인 곳에 저장된 요소를 삭제
			}
		}
		print(list1, list2);
		
	}	// main의 끝
	static void print(ArrayList list1, ArrayList list2) {
		System.out.println("list1 : " + list1);
		System.out.println("list2 : " + list2);
		System.out.println();
	}
}
